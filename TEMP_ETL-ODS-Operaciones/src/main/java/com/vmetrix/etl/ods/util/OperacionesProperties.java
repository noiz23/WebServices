package com.vmetrix.etl.ods.util;

public class OperacionesProperties {
	
	private String file_repository;
	private String driverClassName;
	private String url;
	private String username;
	private String password;
	private String procesaTuring;
	private String scheduler;
	private String validaPdate;
	
	
	

	public String getScheduler() {
		return scheduler;
	}
	public void setScheduler(String scheduler) {
		this.scheduler = scheduler;
	}
	public String getFile_repository() {
		return file_repository;
	}
	public void setFile_repository(String file_repository) {
		this.file_repository = file_repository;
	}
	public String getDriverClassName() {
		return driverClassName;
	}
	public void setDriverClassName(String driverClassName) {
		this.driverClassName = driverClassName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProcesaTuring() {
		return procesaTuring;
	}
	public void setProcesaTuring(String procesaTuring) {
		this.procesaTuring = procesaTuring;
	}
	public String getValidaPdate() {
		return validaPdate;
	}
	public void setValidaPdate(String validaPdate) {
		this.validaPdate = validaPdate;
	}

}
